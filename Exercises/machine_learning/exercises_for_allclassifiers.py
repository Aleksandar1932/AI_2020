def split_train_test(ds):
    train_test_ratio = 0.8

    return (ds[:int(len(ds) * train_test_ratio)], ds[int(len(ds) * train_test_ratio):])


def split_subset(subset):
    subset_x = [row[:-1] for row in subset]
    subset_y = [row[-1] for row in subset]
    return subset_x, subset_y


def calculate_accuracy(pred, actual):
    correct_samples = 0
    for (p, a) in zip(pred, actual):
        if p == a:
            correct_samples += 1
    return correct_samples / len(pred)


def calculate_precision(pred, actual):
    # прецизност = TP / (TP + FP)
    tp = 0  # tocno pozitivni
    fp = 0  # gresno pozitivni
    for (p, a) in zip(pred, actual):
        if p == 1:
            if a == 1:
                tp += 1
            else:
                fp += 1
    if (tp + fp) == 0:
        return 0
    else:
        return tp / (tp + fp)


def calculate_recall(pred, actual):
    # recall = TP / (TP + FN)
    tp = 0
    fn = 0

    for (p, a) in zip(pred, actual):
        if p == 1 and a == 1:
            tp += 1
        if p == 0 and a != 0:
            fn += 1

    if (tp + fn) == 0:
        return 0
    else:
        return tp / (tp + fn)


