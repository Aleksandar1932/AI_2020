def split_dataset(ds, rate):
	return ds[int(((100 - rate) / 100) * len(ds)):], ds[:int(((100 - rate) / 100) * len(ds))]


def split_subset(subset):
	subset_x = [row[0:-1] for row in subset]
	subset_y = [row[-1] for row in subset]
	return subset_x, subset_y


def calculate_accuracy(pred, exact):
	correct_samples = 0
	for (p, e) in zip(pred, exact):
		if p == e:
			correct_samples += 1
	return correct_samples / len(pred)


